    update(flag,step_will,in_combat){
		
        if(flag==3){
            if(!this.walk_animation_started){
				
                this.walk_animation_started = true;
                this.start_animation();
            }
                this.update_animation();
                this.going_to_rest = false;
                this.going_to_rest_started = false;
			
        }
        
        else if(flag==1){
			if(in_combat){
            if(!this.rest_animation_started){
                this.rest_animation_started = true;
                this.start_rest_animation();
                
            }
                this.update_rest_animation();
                this.going_to_rest = false;
                this.going_to_rest_started = false;
				
			}
			else{
				
			this.forward=true
       const current_config = this.x_bot.get_current_configuration();
            if(!this.going_to_rest_started){
				this.start_punch=false;
                const rest_conf = this.x_bot.rest_configuration;
    
                this.going_to_rest_animation_walk_animation = new Interpolation(this.x_bot);
                this.going_to_rest_animation_walk_animation.init(current_config, rest_conf, 400);
                this.going_to_rest_animation_walk_animation.start();
                this.going_to_rest_started = true;

                this.clock_to_rest.start();
                this.start_time_to_rest = this.clock_to_rest.getElapsedTime();
            }
            if(this.clock_to_rest.getElapsedTime()- this.start_time_to_rest > 0.4){
                this.going_to_rest = false;
                this.going_to_rest_started = false;
            }
            this.going_to_rest_animation_walk_animation.update();
		
		}
			
        }
        
        else if(flag==2){
            if(!this.start_punch){
                this.start_punch = true;
                this.start_animation_punch();
                
            }
                this.update_animation_punch();
                this.going_to_rest = false;
                this.going_to_rest_started = false;
            
		}
		
		else if(flag==4){
			if(!this.start_defense){
				
                this.start_defense = true;
                this.start_animation_defense();
                
            }
                this.update_animation_defense();
                this.going_to_rest = false;
                this.going_to_rest_started = false;
		}
        else{
            this.x_bot.set_rest_configuration();
        }
    }


